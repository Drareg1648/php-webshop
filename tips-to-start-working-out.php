    <h2>Fitness program: 5 steps to get started</h2>
    <b>Are you thinking about starting a fitness program? Good for you! You're only five steps away from a healthier lifestyle.</b>
    <p>Starting a fitness program may be one of the best things you can do for your health. Physical activity can reduce your risk of chronic disease, improve your balance and coordination, help you lose weight â€” and even improve your sleep habits and self-esteem. And there's more good news. You can start a fitness program in only five steps.
    </p>
    <ul>
        <li>1. Assess your fitness level</li>
        <p>You probably have some idea of how fit you are. But assessing and recording baseline fitness scores can give you benchmarks against which to measure your progress. To assess your aerobic and muscular fitness, flexibility, and body composition, consider recording:
            <ul>

            <li>Your pulse rate before and immediately after walking 1 mile (1.6 kilometers)</li>
            <li>How long it takes to walk 1 mile, or how long it takes to run 1.5 miles (2.41 kilometers)</li>
            <li>How many standard or modified pushups you can do at a time</li>
            <li>How far you can reach forward while seated on the floor with your legs in front of you</li>
            <li>Your waist circumference, just above your hipbones</li>
            <li>Your body mass index</li>
        </p>
        </ul>
        <li>2. Design your fitness program</li>
        <p>It's easy to say that you'll exercise every day. But you'll need a plan. As you design your fitness program, keep these points in mind:</p>
        <ul>
            <li>Consider your fitness goals.</li>
            <li>Create a balanced routine. </li>
            <li>Start low and progress slowly. </li>
            <li>Build activity into your daily routine.</li>
            <li>Plan to include different activities.</li>
            <li>Try high-interval intensity training.</li>
            <li>Allow time for recovery.</li>
            <li>Put it on paper. </li>
        </ul>
        <li>3. Assemble your equipment</li>
        <p>You'll probably start with athletic shoes. Be sure to pick shoes designed for the activity you have in mind. For example, running shoes are lighter in weight than cross-training shoes, which are more supportive.

            If you're planning to invest in exercise equipment, choose something that's practical, enjoyable and easy to use. You may want to try out certain types of equipment at a fitness center before investing in your own equipment.
            
            You might consider using fitness apps for smart devices or other activity tracking devices, such as ones that can track your distance, track calories burned or monitor your heart rate.</p>
        <li>4. Get started</li>
        <b>Now you're ready for action. As you begin your fitness program, keep these tips in mind:</b>
        <ul>
            <li>Start slowly and build up gradually. </li>
            <li>Break things up if you have to.</li>
            <li>Be creative. </li>
            <li>Listen to your body. </li>
            <li>Be flexible. </li>
        </ul>
        <li>5. Monitor your progress</li>
        <p>Retake your personal fitness assessment six weeks after you start your program and then again every few months. You may notice that you need to increase the amount of time you exercise in order to continue improving. Or you may be pleasantly surprised to find that you're exercising just the right amount to meet your fitness goals.

            If you lose motivation, set new goals or try a new activity. Exercising with a friend or taking a class at a fitness center may help, too.
            
            Starting an exercise program is an important decision. But it doesn't have to be an overwhelming one. By planning carefully and pacing yourself, you can establish a healthy habit that lasts a lifetime.</p>

    </ul>
